int		ft_is_prime(int nb)
{
	int		i;
	int		m;
	int		f;

	m = nb / 2;
	i = 2;
	f = 1;
	if (nb <= 0)
		return (0);
	if (nb <= 1)
		return (0);
	while (i <= m)
	{
		if (nb % i == 0)
		{
			f = 0;
		}
		i++;
	}
	return (f);
}
